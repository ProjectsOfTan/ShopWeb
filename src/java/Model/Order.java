package Model;

/**
 *
 * @author wth0z
 */
public class Order {
    private int id;
    private String email;
    private String order_date,total_price;

    public Order() {
    }

    public Order(int id, String email, String order_date, String total_price) {
        this.id = id;
        this.email = email;
        this.order_date = order_date;
        this.total_price = total_price;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getOrder_date() {
        return order_date;
    }

    public void setOrder_date(String order_date) {
        this.order_date = order_date;
    }

    public String getTotal_price() {
        return total_price;
    }

    public void setTotal_price(String total_price) {
        this.total_price = total_price;
    }
    
    
    
}
